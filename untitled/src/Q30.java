public class Q30 {
    public static void main(String[] args) {
        int x;
        x= 12 - 4 * 2;
        System.out.println("12 - 4 * 2 : "+x);      //12 - 4 * 2 : 4
        x= (12 - 4) * 2;
        System.out.println("(12 - 4) * 2 : "+x);    //(12 - 4) * 2 : 16
        x= 12 - (4 * 2);
        System.out.println("12 - (4 * 2) : "+x);    //12 - (4 * 2) : 4
    }
}
