class Q4{
    public static void main(String args[]){
        System.out.println(10+20+30);		//prints 60
        System.out.println("10+20+30");	//prints 10+20+30
        System.out.println("10+20"+30);	//prints 10+2030
        System.out.println("10"+"20"+"30");	//102030
        System.out.println("10"+20+30);	//102030
        System.out.println(10+20+"30");	//3030
        System.out.println(10+"20"+30);	//102030
    }
}
